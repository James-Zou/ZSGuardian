package com.unionhole.zsguardian.repository;

import java.util.List;
import java.util.Optional;

import com.unionhole.zsguardian.entity.User;
import org.springframework.data.domain.Page;
import org.springframework.data.domain.Pageable;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.Query;
import org.springframework.data.repository.query.Param;
import org.springframework.stereotype.Repository;

@Repository
public interface UserRepository extends JpaRepository<User, Long> {
    User findByUsername(String username);
    boolean existsByUsername(String username);
    boolean existsByPhone(String phone);
    boolean existsByEmail(String email);
    
    @Query("SELECT u FROM User u WHERE u.username = :username AND u.password = :password")
    Optional<User> findByUsernameAndPassword(@Param("username") String username, @Param("password") String password);

    Page<User> findByUsernameContainingOrEmailContainingOrPhoneContaining(
        String username, String email, String phone, Pageable pageable);

    List<User> findByUsernameContaining(String query);
} 